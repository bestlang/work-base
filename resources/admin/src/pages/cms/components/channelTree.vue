  .l-tree-containner{
    min-width: 200px;
    padding: 20px;
    border-right: 1px solid #f4f4f4;
    flex-shrink: 0;
  }
  .custom-tree-node {
    flex: 1;
    display: flex;
    align-items: center;
    justify-content: space-between;
    font-size: 16px;
    padding-right: 8px;
  }
</style>
<template>
      <div class="l-tree-containner">
          <view v-if="title">{{title}}</view>
          <el-tree
          @node-click="handleNodeClick"
          icon-class="el-icon-caret-right"
          :default-expand-all="true"
          ref="tree"
          :data="treeData"
          node-key="id"
          :props="customProps"
          :highlight-current="true"
          :expand-on-click-node="false">
                <span class="custom-tree-node" slot-scope="{ node, data }">
                  <span><span v-if="!data.children.length" class="iconfont">&#xe92a;</span>{{ node.label }}</span>
                </span>
        </el-tree>
      </div>
</template>
<script>
  import {mapGetters} from 'vuex'
  export default {
    data(){
      return {
        customProps: {
          children: 'children',
          label: 'name'
        }
      }
    },
    props:{
        title:{
            type: String,
            default: ''
        },
        selectedKey:{
            type: Number,
            default: 0
        }
    },
    computed:{
        ...mapGetters([
            'currentChannel',
        ]),
      treeData(){
        return this.$store.getters.channels;
      }
    },
    methods:{
      handleNodeClick(...params){
        this.$emit('nodeClick', params);
      }
    },
    async mounted() {
      this.$store.dispatch('toggleState');
      await this.$store.dispatch(this.$types.CMS_CHANNELS);
      console.log(`currentChannel`, this.currentChannel)
      this.$refs.tree.setCurrentKey(this.selectedKey)
    }
  }
</script>
